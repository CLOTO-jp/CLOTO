        6-1-0　初制作のすゝめ
        いよいよ１から全ての制作フローを経て、一つのWebサイトを作る段階に来ました。[プログラミングで「伝える」コース]という名の通り、Web制作は「伝えたいこと」をインターネットを通じて伝えるための手段です。ここを再度押さえておきましょう。
        この事実からどのようなことがわかるかというとWeb制作で一番重要なことはコーディングではないということです。
        元も子もない話のような気がしますがそういうわけではありません。もちろんコーディングも重要です。Class3以降実際にHTMLやCSS、JavaScriptやフレームワークについて学習してきましたが、それらは「伝えたいこと」を効果的に表現するための具体的手法なのです。
        Web制作では実際にコードを書き始める前に、 伝えたいことは何か どのように表現し、伝えるか
        デザインはどうするのか イラストや写真、動画などは何を使うか どのようなWebサイトにするのか
        を決める必要があります。この見通しが全て立ったらやっとコーディングです。どんなに簡単に思える制作目標であっても、間違ってもコーディングありきで、突然始めるようなことは実践的ではないのでやめましょう。
        実務でのWeb制作の現場でも、依頼主が何を伝えようとしているのか、綿密に打ち合わせをすることから始めます。伝えたいこと、キャッチコピー、素材、サイトの雰囲気、操作性、外部との繋がり。あるいはここに分析ツールを埋め込んで閲覧行動の分析を可能にしたり、お問い合わせフォームを入れるなどのニーズにも答えていく必要があります。この仕様をしっかりと確定させることこそがWeb制作で一番重要なことです。ここに価値があるのです。コーディングするだけでは、全ての指示がないと制作できない状態になってしまいます。
        トータルでWeb制作を行える力を身につけていきましょう。
        CLOTOには言語ごとにコミュニティという自習室が設けられています。Class6以降はこれまで以上に積極的に、コミュニティを利用しましょう。検索してもわからないことはコミュニティのチャット欄で質問してみましょう。タイムラインに流してもいいでしょう。これまで以上に調べること、すぐアウトプットしていくことが重要です。逆に質問されたら、いいアウトプット機会と捉えて回答してあげましょう。もし、質問が分からなければこそっと調べることで自分のインプット学習にもなります。
        6-2-0　「自己紹介HP」を創る
        Class6では一律に「自己紹介」のWebページを創ってみましょう。チュートリアル学習、参考書学習、模写学習で学んだことを活かしながら、自分を表現しましょう。今回の伝えたいことは「あなた自身について」です。あなたの特徴、良いところを余すことなく伝えましょう。表現方法は自由です。Class5で見てきたWebデザイン集から着想を得ることも重要でしょう。当然本名である必要もありません。ハンドルネームで構いませんし、情報が全て真実である必要もありません。
        このコースに取り組む学習者がこの「自己紹介」ホームページ制作に取り掛かります。是非素敵なWebページに仕上げてください。
        6-2-1　「自己紹介」HP制作における必須項目 必須項目は以下の通りです。 プロフィール写真
        名前（ハンドルネーム可能） SNSリンク ヘッダー・ボディ・フッターからなるページを2ページ以上
        ボタン 二種類以上のフォント JavaScriptを用いた「お問い合わせフォーム」 6-2-2　テーマを決める
        まずテーマを定めましょう。もちろん今回のテーマは「自己紹介」なのですが、より細かく決めていきましょう。
        自分のどんな点をピックアップして伝えるのか。
        例えば「明るくてアウトドアが大好きな自分」なのか。「クールでインテリジェンスな雰囲気を醸し出す自分」なのか。「趣味に全てを捧げる自分」なのか。
        まずはこのように一連の制作活動全てに関わる根幹となるテーマをしっかりと言語化しましょう。Webページは基本的に、固定されており、また視覚情報に頼られます。日常会話以上に論理的で、主張が一貫されている必要があります。しっかりと最初に言語化しておきましょう。
        「旅行が大好きな大学生の青年」と「起業家として新しい価値を問う30歳の男性」ではWebページでの表現の仕方はまるで違うはずです。自分という人間を、どのような人間と捉え、どう伝えていくか。
        じっくりと考えてみましょう。 6-2-3　ページ構成を考える
        「伝えたいこと」であるテーマを決めることができれば、次は実際にページ構成を考えていきましょう。ページは何枚用意して、それぞれのページにどのようなメッセージを持たせるのか。これの概要を定めていきましょう。
        特別なツールを使う必要は全くありません。裏紙やノートと鉛筆があれば十分。先ほど考えたテーマを念頭に、要素を分解していきましょう。
        ホームページはどうするか。ホームの他にはどのような情報を載せるページを作りましょうか。日々の活動報告、趣味、経歴などなど。
        Class6では２〜３ページ程度作りましょう。 6-2-4　ページごとに載せる情報を考える
        ページ割りが決まれば、次はページごとに載せる情報を考えていきましょう。全てを載せることはナンセンスです。
        1ページでテーマは一つに絞りましょう。読み進めるにしたがって次から次へと違う内容が出てくると、非常に読みにくいサイトになります。「伝えたいこと」もブレてしまい、支離滅裂な文章、構成になってしまうので気をつけましょう。
        こちらも紙と鉛筆を持って、ページごとに書きたいことの概要をメモしましょう。
        6-2-5　各ページの雰囲気を考える
        各ページごとにテーマからブレない程度に雰囲気をつけていきましょう。ページしたときに心奪われるような、ワクワクするような閲覧体験を作りましょう。特に今回のお題は「自己紹介」ですので、ページごとに少し違った雰囲気で、色々な面を表現してもいいかもしれません。
        6-2-6　配置などを手書きする
        ページ構成、ページごとの情報、雰囲気が決まったら、今度は各ページのレイアウト、配置を定めていきましょう。Web制作では効果的な写真、アニメーション、フォント、配色など、さまざまな方法で「伝えたいこと」を表現します。どのような写真や文字、文章をどこに配置するのか。概要を定めましょう。
        これも紙と鉛筆で全く問題ありません。まずはページの構成を決めましょう。手書きでささっとキャッチコピーのエリアやテキストのエリア、画像のエリアなどをメモをします。そこから徐々に細かく、テキストはどのような内容にするか。画像はどのような素材をどう使うかを決めていきましょう。Webページは、デバイスのディスプレイという限られた空間に表現しなくてはなりません。ここに適切な情報を適切な箇所に配置していく必要があります。Class5で紹介したWebデザイン集などを参考にしながら
        最近ではAdobeXDやFigmaなどの、非常に便利なUIデザインツールがあります。大変便利で高性能なものですがこれを使うにもやはり最初は手書きのメモに勝るものはないでしょう。Class7以降では、より詳細部分を具体的に検討する際や、コーディングに踏み切る前、プレビューする場合などに積極的な利用を推奨します。
        6-2-7　素材を用意する
        ここまでいかがだったでしょうか。これから作ろうとしている自己紹介Webサイトのイメージはできたでしょうか。イメージができたら、自己紹介HPで使用しようと考えている画像を用意しましょう。画像が用意できたら必要な加工を施す必要があります。画像サイズ、切り抜き、彩度、透明度など、必要な状態に加工してみましょう。
        Adobe
        PhotoshopやIllustratorといった、有名な有料の画像編集ソフトもありますし、最近では無料でも高品質のものも増えています。Microsoft
        OfficeのPowerPointなども簡単な画像編集はできるので使ってみてもいいでしょう。スマートフォンなどで編集できるアプリケーションもあるのでいろ色検索をかけたり、調べたりする必要があるでしょう。
        6-2-8　コーディングする
        ページ構成や、テキストの内容、レイアウトの決定や素材の準備などのWeb制作の準備ができたらいよいよコーディングです。Class3〜Class5で学習してきたことを活かして実際にコードを書いていきましょう。ぜひ自己紹介HPにおける必須項目を入れてみてください。
        まずはHTMLとCSSを用いてある程度の自己紹介ページ静的な部分の骨格や体裁を整えていきましょう。それが出来上がってきたら、JavaScriptで動きをつけていきましょう。必須項目にある　JavaScriptを用いた「お問い合わせフォーム」　の作成では閲覧者がフォームに記述したデータをサイト運営者が受け取れる仕組みを作りましょう。
        わからないことは検索と質問を使い分けながら、すぐに調べましょう。キリの良い所でカルテを書いて、実際にコーディングや調べ物をしていく過程で学んだことの記録をつけることも忘れずに継続しましょう。
