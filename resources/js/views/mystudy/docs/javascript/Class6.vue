        6-4-0　Webページ公開のすゝめ
        初のWeb制作はいかがでしょうか。実際に思い描いていたものや「伝えたいこと」がWebブラウザ上に表示されれば、面白く思えてきたのではないでしょうか。
        実際に「自己紹介HP」の制作ができれば、実際にそれをインターネット上に公開してみましょう。これまでは自分のPCの中でファイルにコーディングしていく、いわゆる「ローカル環境でのWeb制作」をしてきました。これをインターネット上に公開すればURLリンクなどを発行することで、実際に多くの人にWebサイトをみてもらうことができます。
        ローカル環境で制作したWebサイトのファイルはサーバへ保存することでインターネット上からアクセスできるようになります。
        Class6では「自己紹介HP」の制作が課題でした。ここではローカル環境での制作ができれば良いのでWebページを公開することは強制ではありません。もし公開してみてもいいと思える方は、載せてはいけない情報がないか確認した上で、良い学習機会になると思うので是非チャレンジしてみてください。
        完成したWebサイトを公開した場合はURLを、そうでない場合は完成したWebサイトのスクリーンショットの公開することを以てClass6の完了とします。
