Webサイトをインターネット上に公開するためには、ドメイン(URLの一部)と、Webサーバが必要になります。その他にも留意すべき点を記しておきます。

9-1-1　ドメインの用意
Class2でWebの仕組みについて説明したときに、URLについて触れました。URLはUniform Resource Locatorの略で、統一資源位置指定子と言います。インターネット上の情報の所在を表記する方法です。主にWebページの保存場所や電子メールの宛先などを示しています。日本ではURLのことをアドレスと呼ぶことがありますが、MACアドレスやIPアドレスなどと紛らわしいため、技術用語としては好まれません。

IPネットワークでここのコンピュータを識別し、接続先を指定するために使用される名称の一部であり、URLの一部にもなるのがドメインです。

ドメインはインターネットにおいては世界中で絶対に重複しないようにするため、ICANN(アイキャン)という非営利期間によって一言管理されています。また、世界中で重複が許されないというWWWの仕組み上、ドメイン名を使用する権利は、一番初めに申請したものに与えられるようになっています。ドメインは登録から最低１年、更新手続きを行うことで、期間を延長することが可能です。

ドメインにはいくつか種類があり、有料のものと無料のものがあります。

ドメインについてさらにみていきましょう。

例えばCLOTOのURLですが、

https://cloto.jp/

のようになっています。この「cloto.jp」の部分がドメインになっています。メールアドレスで言うと、@以降の箇所がドメインになっています。

この「.jp」の箇所がドメインの種類になるわけですが、他には以下のようなものがあります。

.com　：　一般企業や個人での利用が多い
.net　：　ネットワークサービスを意識させる
.org　：　組織やグループなどの非営利団体を表す
.info　：　情報発信、お問い合わせなどを表す
.biz　：　商用利用、ビジネスを想起させる
.jp　：　所在が日本にあることを表す
これら以外にも様々な種類のドメインがあります。通信会社ごとにメールアドレスのドメインを持っていることもあります。これらどのドメインを選んでも基本的に違いはありません。

「.jp」は日本に住所がないと申請できません。日本に住所があることは世界的に一定の信用を担保できるなどの利点があります。そう言う観点から言うと「.co.jp」のドメインは普通のドメイン取得に必要なものに加えて、企業としての登記情報が必要になるため、信頼性の高さが別格です。

独自ドメインを取得するためには、ドメイン取得サービスで、取得したいドメインが既に利用されていないか調べます。ドメインは企業名や、サービス名、サイト名などと一致している方がよく、短く覚えやすいものがいいでしょう。

どれだけ理想に近いドメイン名が取得できるか、そしてドメイン取得にかかる値段を鑑みて、どの種類のドメインを取得するか決めましょう。

9-1-2　レンタルサーバの用意
Class2で紹介したように、Webサイトをインターネット上に公開するには、サーバが必要です。かつては、自分で高性能のサーバ専用のコンピュータを用意して、環境構築をする必要がありましたが、近年は月額料金でレンタルできるサーバが普及してきています。以前に比べ格段に安く、またオンラインで簡単に申し込むことができるので、非常に便利になってきています。

レンタルサーバーにも様々な種類があるので、レビューなどを見て良いものを選びましょう。セキュリティ対策や常時SSL化ができるかに注意しながら、容量と費用とを考慮して用途にあったものを選びましょう。

有名なものは後ほど簡単に紹介します。

9-1-3　常時SSLにする
Web制作だけに関わらず、プログラミング独学をしていく先のエンジニア、開発者という仕事は、セキュリティと隣り合わせの職業です。インターネットは世界中を繋ぎ、世の中を便利にした一方で、常に情報が世界中に拡散されてしまったり、公開していない情報を狙ってインターネット経由で攻撃を受けることがあります。

これらは完全に伏ぐことができません。一方で情報を扱う以上、あるいはインターネットという公共の場にサービスを提供する以上、少しでもセキュリティ対策を心がけることは開発者の責務です。

Web制作の現場における最も初歩的なセキュリティ対策が常時SSL化です。インターネットなどのコンピュータネットワークにおいてセキュリティを要求される通信を行うためのプロトコル(通信規約)であるTLS(Transport Layer Security)というものがありますが、これの別名がSSL(Secure Sockets Layer)です。

URLの先頭に「http://」というものがついていると思います。これはHTTP(Hypertext Transfer Protocol)というWebページなどのコンテンツの送受信に用いられるプロトコルを示しています。SSLをHTTPと組み合わせることでHTTPSという安全なプロトコルを実現します。CLOTOのURLもよく見ると

https://cloto.jp/

先頭が「https」になっているかと思います。このようにセキュリティ対策をするために、常時全てのページに対して、HTTPとSSlの両方のプロトコルを組み合わせた比較的安全なHTTPSのプロトコルにすることを「常時SSL化」と言います。

Webブラウザによっては、この常時SS化をしておかないと、「このページは保護されていません」などの警告文が出るようになっています。あなたの作ったWebサイトを訪問したときに、警告文が出れば、閲覧者から信頼を得ることができません。

常時SSL化をし「https」にしておくと他にもメリットがあります。

検索エンジンでの検索順位が優先されるため、SEO対策になります。サイト分析の精度も上がります。またページ表示速度も早くなります。

レンタルサーバーを選ぶときには、常時SSL化ができるかを確認し、いざWebサイト公開の際には常時SSL化をしましょう。

9-1-4　FTPクライアントソフト
FTPクライアントソフトとは、作成したWebページをサーバにアップロードするためのソフトウェアです。ホームページ作成ソフトや、クラウド型のCMS(Content Management System：コンテンツ管理システム)にはすでに同等の機能を持ち合わせているものも多いので近年では必ずしも必要としません。

一方で[プログラミングで「伝える」コース　JavaScriptを軸に学ぶロード]では、こういったサービスを利用していない前提ですのでいくつか有名なFTPクライアントソフトを紹介します。この他にもたくさんあるので調べて使ってみましょう。

