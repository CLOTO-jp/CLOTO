9-2-1　GitHub Pages
GitHub PagesについてはClass8でも紹介しました。静的なWebページを簡単にインターネット上に公開できるWebサービスです。更新も簡単に加えることができます。

9-2-2　Heroku
Herokuとは環境開発用のサーバサービスです。Emailアドレスさえあれば登録可能で、１サービスのみ無料で利用することができます。基本的にはWebアプリ向けのサービスなので、HTMLを中心とする静的なWebサイトの場合には、PHPなどでファイルを作ることで、Webアプリと認識させることで、表示が可能になります。少し応用的な利用法になりますが、今後、Webアプリ開発なども想定しているのであれば、ここで少し調べてみるのもありかもしれません。

9-2-3　AWS Amazon S3
AWS(Amazon Web Service)はAmazon社の提供する世界最大のクラウドサービスです。このサービスの一環に Amazon S3(Amazon Simple Storage Service)というものがあります。Webサイトのコンテンツ配信やバックアップに利用できます。S3は静的なWebページのみに対応しています。

AWSは全世界的に利用されている世界最大のクラウドサービスです。Amazon S3以外にも非常に多くのサービスを提供しており、業界標準にもなりつつあるサービスです。エンジニア、開発者として知っておいて損のないサービスでしょう。

利用するしないに関わらず、どこかで時間をとって調べてみる価値がありそうですよ。

9-2-4　SAKURA internet
SAKURA internetは国内で有力なレンタルサーバ、クラウドコンピューティングを提供するサービスです。「さくらのレンタルサーバ」、「さくらVPS」などいくつかのサービスを提供しています。ドメインの取得、レンタルサーバの利用が可能です。ファイルをサーバへアップするためにFTPソフトを使う方法と、独自のファイルマネージャーを使う方法のふたつが用意されています。

9-2-5　ロリポップ！
