        4-3-2　公式ドキュメント
        公式ドキュメントとは言語の開発者、団体などがまとめているドキュメントのことで、実はチュートリアルから、各要素、文法、構文などが事細かに記述されています。命名規則などのルールも載っていることが多いです。
        開発者や団体などが提供していない場合、第三者が作成した要素リファレンス集などもあります。
        なかなか公式ドキュメンをしっかり読んで学習する人は少ないですが、現役エンジニアの多くが「困ったら公式ドキュメント」と言われます。要は、原点に帰れということです。学習者にとってもこの原点を知っているかいないかの差は大きいのでないでしょうか。ぜひ学習に役立ててみてください。
        一方で、公式ドキュメントは英語で記述されていることが多いです。専門的な用語も多いのでなかなか翻訳しながら読み進めていくとなると、ハードルも上がるかと思います。ので、無理して一周目の教材学習で用いる必要はないのかなと思います。しかし、「こういうものがある」ということは絶対に知っておくべきでしょう。
