        JavaScriptはその特徴から、主にクライアントサイドで使われるプログラミング言語ですが、近年ではNode.jsといった、サーバサイドでのジャバスクリプト実行環境も生まれたことにより、その活躍の幅をサーバサイドまで広げています。
