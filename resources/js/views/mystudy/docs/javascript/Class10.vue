10-2-1　レスポンシブ対応はしたか
Class7で触れたように、様々なディスプレイサイズのデバイスからWebブラウジングされるようになった今日では、それぞれのディスプレイサイズに応じたWebデザインが求められます。これに対応するデザインをレスポンシブWebデザインと言います。

二種類のコードを用意しなくてもCSSやフレームワークを駆使することで簡単に対応させることもできます。まだ取り組んでいない方は、ぜひ取り組んでみてください。

10-2-2　SEOとは
これまでも少し触れてきましたが、SEOとはSearch Engine Optimizationの略で「検索エンジン最適化」と言います。これはWebブラウザの検索エンジンのオーガニックな検索結果において特定のWebサイトが検索結果の上位に用事されるよう、検索エンジンのアルゴリズムを逆利用するべくWebサイトの構成などを調整することです。難しくなりましたが、要は検索結果上位にWebサイトを表示させるための取り組みのことです。

Webサイトは「伝えたいこと」があるから作られるわけで、少しでも露出を増やし、多くの人に見てもらうことを目的に作られることが多いです。特にWebを利用したデジタルマーケティングの現場では、このSEOが非常に重要視されています。

検索エンジンのアルゴリズムは公開されていないものも多いので、公式から出たわではない情報がほとんどです。しかしこれまでに多くのマーケティング担当者やエンジニアが、さまざまな施策を行う中で得られた知識が蓄積されて、ある程度の共通認識が作り上げられている状況もあります。試しにGoogle ChromeというWebブラウザのSEOの方法を検索してみましょう。

もし今回のWeb制作で必要なくても、SEOのノウハウは非常に重宝されるので、調べたり、試験的に実装してみることをお勧めします。

10-2-3　分析ツールの埋め込み
「伝える」ことが目的のWebサイトにおいて、そのWebサイトが本当に価値を発揮しているのか、定量的に確認することも重要です。さまざまな分析ツールがあります。

1日何人が訪問し、そのうちどれほどがしっかりと閲覧しているか。何分間Webサイト上にるのか。直帰率はどれくらいか。それらの推移はどうなっているか。などを分析してくれるツールもあります。Google Chromeにおける、GoogleAnalyticsというサービスはその代表と言えるでしょう。

また一風変わったものにヒートマップという分析ツールもあります。これは閲覧者のカーソルや指の動きなどを元に、ページのどの辺までみられているか。どこに興味を示しているかなどの度合いをサーモグラフィーのように色分けで視覚的に示してくれるものです。こちらもWeb制作者には必須のツールと言えますので、導入しておきましょう。



