        3-2-3　Paizaラーニング
        PaizaはITエンジニア向けの転職・就活・学習サービスです。ITエンジニア・プログラマ専門の総合求人・学習サービスが、プログラミング学習領域にもコンテンツを出しているという印象です。
        従って、ゴールはIT就職、転職となっていることが多く、それを目的に利用しているユーザーが多いことが特徴です。
        そのPaizaが提供している初心者向け学習サービスが「Paizaラーニング」です。3~4分の短い動画を視聴しながら自分の学習環境で学習をしていきます。
        2021年5月現在で有料プランでは1,304本の学習動画を超可能です。有料プランの料金は月額¥1,078で学び放題です。
        Paizaラーニングの長所は、 多くの言語やツールの動画講座があり、幅広い学習ができる。
        転職や就職を目標としたカリキュラムのため、スキルチェックや、就活準備や現役エンジニアの小話など、他のサービスにはないコンテンツがある。
        Paizaの注意点は、
        プログラミング独学というより、どちらかというと、転職を目的としたスキルの獲得が目的である。
        スキルの獲得を目指す転職サービスであるという本質から、どちらかというとプログラミングスクールに近い印象を受けます。
        Paizaラーニングでも「これはチュートリアル学習である」と理解して取り組むことが重要です。
        自分で0から価値を生み出すようなことを目的として設計されているカリキュラムではないので、
        しっかりと次のステップを思い浮かべながら、チュートリアル学習教材として有効活用しましょう。
        3-2-4　Udemy
        UdemyはITエンジニアリングやデザイン、ビジネススキル、音楽など世界中の技術を持つ人々が自発的に講座を作成し、売り出す、学習講座のプラットフォームです。
        そういった特性上から少しレベルの高い教材も多いのでClass3で学習するには多少時期尚早な気もします。
        講座ごとに単価が設定されているので、自分にあった講座を見極めることも必要ですし、Class4で再度おすすめしようと考えています。
        この段階で興味のある方はぜひ調べてみてください。 3-2-5　YouTube
        言わずと知れたYouTubeですが、最近はプログラミング学習系ユーチューバーも増えていて、非常にわかりやすく面白いコンテンツで溢れています。
        当然無料です。一方でしっかりと一つの講座として再生リストとしてまとめられていたり、講座作成者の主観的なお話も聞けるのも特徴です。
        YouTubeの長所は、 全て無料の割にクオリティの高い講座が多い。
        講座作成者の生の声を聞ける。それが現役フリーランスエンジニアならその実体験を元に学習できる
        コメント欄で他の視聴者と交流したり、YouTuber自身に質問することができる。たまに返信が返ってくる。
        YouTubeの注意点は、
        あくまで個人の作成した無料講座なので、コードを間違えていたり、言葉や解説の間違いを訂正していないことがある。
        解説の仕方にYouTuberの特徴が出るので、親切に解説してくれる人もいればそうでない人ももちろんいるのでチャンネルを選ぶ見極めが重要。
        オンラインサロンへの入口や、スクールへの勧誘になっていることもあるので、最後の最後の肝心なところで動画が切られていることなどもあり、口座の全貌の透明性は有料サービスほど高くない。
        YouTubeは無料で、YouTuberが少しでもわかりやすいように、面白いように工夫しているものが多いので、学習のきっかけとしては良いかもしれません。
        使い慣れた媒体で学習できることを考えるとストレスも少ないです。一方で学習であることを忘れると、大変危険です。
        普段YouTubeを見慣れている皆さんであれば、これは容易に想像つくかと思います。目標を定めて勢いを持って一気に視聴しましょう。

