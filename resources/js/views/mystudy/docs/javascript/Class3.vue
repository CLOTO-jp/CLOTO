        3-2-2　ドットインストール
        ドットインストールは１レッスン３分の動画を見ながら自身の開発環境で学習するサービスです。
        Progateがスライドと用意された環境での学習だったのに対して、ドットインストールは環境開発するところから教材で扱います。目標ごとに一連のレッスン動画が並べられていて順を追って学習することが可能です。
        無料で学習できる箇所もありますが、体型的に学習する場合は有料プランへ移行することも必要かもしれません。
        有料プランは、全レッスンが利用できることに加えて、文字起こしやソースコード、質問機能などリッチな機能を利用することができます。これらを含め、有料プランは月額¥1,080です。
        ドットインストールの長所は 動画教材でわかりやすい。 環境構築から学習できる。
        有料プランが充実している。 レッスンが豊富、学習できる言語やツールも豊富
        ドットインストールの注意点は
