        あくまでチュートリアル＋αなのでこのレッスンだけでは、あくまで基本レベルであるということ
        ドットインストールも「これはチュートリアル学習である」と理解して取り組むことが重要です。しっかりと次のステップを思い浮かべながら、有効活用しましょう。
