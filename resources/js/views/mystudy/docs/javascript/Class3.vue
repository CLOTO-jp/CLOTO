        3-2-1　Progate プログラミングのチュートリアル教材の筆頭といえばこの「Progate」でしょう。
        日本初のサービスで世界中にユーザーがいます。
        累計登録ユーザー数は210万人を超え、近年急成長しているサービスです。
        おそらくみなさんもプログラミング学習をする上で誰かに勧められたり、Twitterやブラウザ上で見かけたことがあることでしょう。
        Progateはその名の通り、プログラミングの門です。入り口にふさわしい教材でしょう。
        Progateは言語やツールごとに学習するコースが設けられています。各コースで基本的な文法や仕組みを学習することができます。
        スモールステップが特徴で、学習するスライド数枚を読んで、次に用意された環境に、スライドで学習したコードを打ち込んでいくことで、プログラミングの基礎を手を動かしながら体験することができます。
        Progateは最初は無料で会員登録をすることができます。基礎レベルの18レッスンを無料で利用可能です。
        コースを全て学習するには有料プランに切り替えることが必要です。2021年5月現在で、月額税込¥1,078で、応用、実戦レベルの全82レッスンを学び放題です。
        Progateの長所は
        ビジュアライズされ、難しい用語を使わない、基本に忠実で親切なサービス。気軽に楽しく学習できる。
        環境構築の必要がなく、ブラウザ上で学習が完結する。PCのスペックを問わずすぐに学習を始められる。タブレットやスマートフォンでも利用可能。
        取り組んでいるユーザーが多い。 Progateの注意点は
        難しい環境構築はProgateが用意してくれているなど、プログラミングの辛い箇所は実感する場面が少ない。
        答えがいつでも閲覧可能で、誤っている箇所もすぐに確認できるので「調べる力」は身につかない。
        Progateはあくまでチュートリアル教材であることを認識せずに利用すると、コースを終えた段階でどうしていいかわからなくなる。
        しかし、Progateの注意点は「これはチュートリアル学習である」と理解して取り組むことで全て解決します。しっかりと次のステップを思い浮かべながら、有効活用しましょう。
